package step04;

public class Exam037_1 {
  public static void main(String[] args) {
    System.out.println();

    int a = 10;
    a = a + 1;
    System.out.println(a);

    int b = 10;
    b++;
    System.out.println(b);

    int c = 10;
    System.out.println(c++);


    c = 10;
    c--;
    System.out.println(c);


    System.out.println();
  }
}
