package step04;

public class Exam037_0 {
  public static void main(String[] args) {
    System.out.println();

    int a = 10;
    a = a + 1;
    System.out.println(a);

    int b = 10;
    ++b;
    System.out.println(b);

    int c = 10;
    System.out.println(++c);


    a = 10;
    --a;
    System.out.println(a);


    System.out.println();
  }
}
