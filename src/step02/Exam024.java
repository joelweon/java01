// 주제: Literal - 정수의 자릿수 구분자
package step02;

public class Exam024{
  public static void main(String[] args) {
    //
    System.out.println(456_7899);   //10진수  미국은 단위를 이렇게 씀.
    System.out.println(4_567_899);
    System.out.println(0x64);  //16진수
    System.out.println(0b0110_0100);  //2진수 0b뒤에는 _ 사용불가

  }

}
